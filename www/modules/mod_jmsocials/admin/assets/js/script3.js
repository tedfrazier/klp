jQuery(document).ready(function(){
    //Slider source
    jQuery('#jform_params_asset-lbl').parents('.control-group').hide();
    JMSlideShow_SourceChange(jQuery('#jform_params_slider_source').val());
    jQuery('#jform_params_slider_source').change(function(){
        JMSlideShow_SourceChange(jQuery(this).val());
    })
})
function JMSlideShow_SourceChange(source){
    switch(source){
        case '1':
            jQuery('#jform_params_jmcategory').parents('.control-group').css({
                display:'block'
            });
            jQuery('#jform_params_jmk2_categories').parents('.control-group').css({
                display:'none'
            });
            jQuery('#jform_params_jmrss_link').parents('.control-group').css({
                display:'none'
            });
            jQuery('#jform_params_jmexternal_link').parents('.control-group').css({
                display:'none'
            });
            jQuery('#jform_params_jmslideshow_count').parents('.control-group').css({
                display:'block'
            });
            break;
        case '2':
             jQuery('#jform_params_jmcategory').parents('.control-group').css({
                display:'none'
            });
            jQuery('#jform_params_jmk2_categories').parents('.control-group').css({
                display:'block'
            });
            jQuery('#jform_params_jmrss_link').parents('.control-group').css({
                display:'none'
            });
            jQuery('#jform_params_jmexternal_link').parents('.control-group').css({
                display:'none'
            });
            jQuery('#jform_params_jmslideshow_count').parents('.control-group').css({
                display:'block'
            });
            break;
        case '3':
             jQuery('#jform_params_jmcategory').parents('.control-group').css({
                display:'none'
            });
            jQuery('#jform_params_jmk2_categories').parents('.control-group').css({
                display:'none'
            });
            jQuery('#jform_params_jmrss_link').parents('.control-group').css({
                display:'block'
            });
            jQuery('#jform_params_jmexternal_link').parents('.control-group').css({
                display:'none'
            });
            jQuery('#jform_params_jmslideshow_count').parents('.control-group').css({
                display:'block'
            });
            break;
        case '4':
             jQuery('#jform_params_jmcategory').parents('.control-group').css({
                display:'none'
            });
            jQuery('#jform_params_jmk2_categories').parents('.control-group').css({
                display:'none'
            });
            jQuery('#jform_params_jmrss_link').parents('.control-group').css({
                display:'none'
            });
            jQuery('#jform_params_jmexternal_link').parents('.control-group').css({
                display:'block'
            });
            jQuery('#jform_params_jmslideshow_count').parents('.control-group').css({
                display:''
            });
            break;
    }
}
